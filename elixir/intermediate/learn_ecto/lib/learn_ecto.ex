defmodule LearnEcto do
  @moduledoc """
  Ecto v3.14 with SQLite (ecto_sqlite3) — Elixir's database layer.

  Ecto is NOT an ORM — it's a toolkit for data mapping and database access.
  It has four main concerns:

    1. Schema     — map structs ↔ database tables
    2. Changeset  — validate and transform data before writing
    3. Query DSL  — composable, safe SQL queries
    4. Repo       — database access API (insert, get, update, delete)

  Setup in mix.exs:
    {:ecto, "~> 3.14"},
    {:ecto_sql, "~> 3.14"},
    {:ecto_sqlite3, "~> 0.18"}

  In application/0:
    extra_applications: [:logger]
  """

  def run do
    IO.puts("\n=== Ecto: Database Layer ===\n")

    # Boot an in-memory SQLite repo for this demo
    {:ok, _} = LearnEcto.Repo.start_link([])
    LearnEcto.Migration.up()

    schemas_and_structs()
    changesets()
    repo_crud()
    query_dsl()
    multi_operations()
    embedded_schemas()

    LearnEcto.Repo.stop()
  end

  # -----------------------------------------------------------------------
  # 1. Schema basics
  # -----------------------------------------------------------------------
  defp schemas_and_structs do
    IO.puts("--- Schema Basics ---")

    IO.puts("""
    # Schema maps a struct to a database table.
    defmodule Blog.Post do
      use Ecto.Schema

      schema "posts" do
        field :title,      :string
        field :body,       :string
        field :published,  :boolean, default: false
        field :view_count, :integer, default: 0
        field :tags,       {:array, :string}, default: []
        belongs_to :author, Blog.User    # adds :author_id foreign key
        has_many   :comments, Blog.Comment
        timestamps()                     # adds inserted_at, updated_at
      end
    end

    # Schema reflection (generated by use Ecto.Schema):
    Blog.Post.__schema__(:source)         #=> "posts"
    Blog.Post.__schema__(:primary_key)    #=> [:id]
    Blog.Post.__schema__(:fields)         #=> [:id, :title, :body, ...]
    Blog.Post.__schema__(:type, :title)   #=> :string
    Blog.Post.__schema__(:associations)   #=> [:author, :comments]
    """)

    # Create a struct directly (no DB needed for struct operations)
    post = %LearnEcto.Post{title: "Hello Ecto", body: "It works!"}
    IO.puts("Post struct: #{inspect(post)}")
    IO.puts("Source table: #{LearnEcto.Post.__schema__(:source)}")
    IO.puts("")
  end

  # -----------------------------------------------------------------------
  # 2. Changesets
  # -----------------------------------------------------------------------
  defp changesets do
    IO.puts("--- Changesets ---")

    # Valid changeset
    params = %{"title" => "My Post", "body" => "Content here"}
    cs = LearnEcto.Post.changeset(%LearnEcto.Post{}, params)
    IO.puts("Valid? #{cs.valid?}")
    IO.puts("Changes: #{inspect(cs.changes)}")

    # Invalid changeset (missing required fields)
    bad_cs = LearnEcto.Post.changeset(%LearnEcto.Post{}, %{})
    IO.puts("Invalid? #{!bad_cs.valid?}")
    IO.puts("Errors: #{inspect(bad_cs.errors)}")

    # put_change / get_change
    cs2 = Ecto.Changeset.put_change(cs, :view_count, 42)
    IO.puts("view_count change: #{Ecto.Changeset.get_change(cs2, :view_count)}")

    # change/2 for internal updates (no casting/validation):
    cs3 = Ecto.Changeset.change(%LearnEcto.Post{}, view_count: 100)
    IO.puts("Internal change valid: #{cs3.valid?}")

    IO.puts("")
  end

  # -----------------------------------------------------------------------
  # 3. Repo CRUD
  # -----------------------------------------------------------------------
  defp repo_crud do
    IO.puts("--- Repo CRUD ---")

    alias LearnEcto.Repo, as: R

    # INSERT
    {:ok, post1} = %LearnEcto.Post{}
      |> LearnEcto.Post.changeset(%{"title" => "First Post", "body" => "Hello world"})
      |> R.insert()
    IO.puts("Inserted: #{post1.id} — #{post1.title}")

    {:ok, post2} = %LearnEcto.Post{}
      |> LearnEcto.Post.changeset(%{"title" => "Second Post", "body" => "More content"})
      |> R.insert()

    # GET by primary key
    found = R.get(LearnEcto.Post, post1.id)
    IO.puts("Get by id: #{found.title}")

    # get! raises if not found
    # R.get!(LearnEcto.Post, 99999)  #=> raises Ecto.NoResultsError

    # GET by field
    by_title = R.get_by(LearnEcto.Post, title: "First Post")
    IO.puts("Get by title: #{by_title.title}")

    # UPDATE
    {:ok, updated} = found
      |> LearnEcto.Post.changeset(%{"title" => "First Post (updated)"})
      |> R.update()
    IO.puts("Updated: #{updated.title}")

    # ALL
    all_posts = R.all(LearnEcto.Post)
    IO.puts("All posts count: #{length(all_posts)}")

    # DELETE
    {:ok, _} = R.delete(post2)
    IO.puts("After delete: #{length(R.all(LearnEcto.Post))} posts")

    # insert_all — bulk insert (returns count, not structs)
    {n, _} = R.insert_all(LearnEcto.Post, [
      %{title: "Bulk 1", body: "body", inserted_at: DateTime.utc_now(), updated_at: DateTime.utc_now()},
      %{title: "Bulk 2", body: "body", inserted_at: DateTime.utc_now(), updated_at: DateTime.utc_now()}
    ])
    IO.puts("Bulk inserted: #{n}")

    IO.puts("")
  end

  # -----------------------------------------------------------------------
  # 4. Query DSL
  # -----------------------------------------------------------------------
  defp query_dsl do
    IO.puts("--- Query DSL ---")

    import Ecto.Query
    alias LearnEcto.Repo, as: R

    # Basic query
    all = R.all(from p in LearnEcto.Post, select: p.title)
    IO.puts("All titles: #{inspect(all)}")

    # Where + order_by + limit
    recent = R.all(
      from p in LearnEcto.Post,
      where: not is_nil(p.title),
      order_by: [desc: p.inserted_at],
      limit: 3,
      select: %{id: p.id, title: p.title}
    )
    IO.puts("Recent 3: #{inspect(recent)}")

    # Composable queries — build pieces and combine
    base_query = from p in LearnEcto.Post, where: not is_nil(p.title)
    final = from p in base_query, order_by: [asc: p.title], limit: 2
    IO.puts("Composed query count: #{length(R.all(final))}")

    # Parameter interpolation with ^ (prevents SQL injection)
    search_term = "Bulk"
    results = R.all(from p in LearnEcto.Post, where: like(p.title, ^"%#{search_term}%"))
    IO.puts("Search '#{search_term}': #{length(results)} results")

    # Aggregate
    count = R.one(from p in LearnEcto.Post, select: count(p.id))
    IO.puts("Total count: #{count}")

    IO.puts("")
  end

  # -----------------------------------------------------------------------
  # 5. Ecto.Multi — atomic multi-step operations
  # -----------------------------------------------------------------------
  defp multi_operations do
    IO.puts("--- Ecto.Multi: Atomic Operations ---")

    alias LearnEcto.Repo, as: R

    result = Ecto.Multi.new()
      |> Ecto.Multi.insert(:post_a,
          LearnEcto.Post.changeset(%LearnEcto.Post{}, %{
            "title" => "Multi Post A", "body" => "part of atomic op"
          }))
      |> Ecto.Multi.insert(:post_b,
          LearnEcto.Post.changeset(%LearnEcto.Post{}, %{
            "title" => "Multi Post B", "body" => "part of atomic op"
          }))
      |> Ecto.Multi.run(:verify, fn _repo, %{post_a: a, post_b: b} ->
          # Custom step — can use previous results
          {:ok, "Created #{a.title} and #{b.title}"}
      end)
      |> R.transaction()

    case result do
      {:ok, %{post_a: a, post_b: b, verify: msg}} ->
        IO.puts("Multi success: #{msg} (ids: #{a.id}, #{b.id})")
      {:error, failed_op, reason, _changes} ->
        IO.puts("Multi failed at #{failed_op}: #{inspect(reason)}")
    end

    IO.puts("")
  end

  # -----------------------------------------------------------------------
  # 6. Embedded schemas (no table needed)
  # -----------------------------------------------------------------------
  defp embedded_schemas do
    IO.puts("--- Embedded Schemas ---")

    IO.puts("""
    # Embedded schemas live inside another struct — stored as JSON
    # in the parent's column (not a separate table).

    defmodule Order do
      use Ecto.Schema
      import Ecto.Changeset

      schema "orders" do
        field :reference, :string
        embeds_one  :billing_address, Address
        embeds_many :line_items, LineItem
        timestamps()
      end

      def changeset(order, params) do
        order
        |> cast(params, [:reference])
        |> cast_embed(:billing_address, required: true)
        |> cast_embed(:line_items, required: true)
        |> validate_required([:reference])
      end
    end

    defmodule Address do
      use Ecto.Schema

      embedded_schema do
        field :street, :string
        field :city,   :string
        field :zip,    :string
      end

      def changeset(addr, params) do
        addr
        |> Ecto.Changeset.cast(params, [:street, :city, :zip])
        |> Ecto.Changeset.validate_required([:street, :city])
      end
    end

    # Usage:
    Order.changeset(%Order{}, %{
      reference: "ORD-001",
      billing_address: %{street: "123 Main", city: "Springfield", zip: "12345"},
      line_items: [%{sku: "WIDGET-01", qty: 3, price: "9.99"}]
    })
    # The address and line_items are stored as JSON in their parent columns.
    # No joins needed — just one row.
    """)
    IO.puts("")
  end
end

# ---- Supporting modules ----

defmodule LearnEcto.Repo do
  use Ecto.Repo, otp_app: :learn_ecto, adapter: Ecto.Adapters.SQLite3
end

defmodule LearnEcto.Post do
  use Ecto.Schema
  import Ecto.Changeset

  schema "posts" do
    field :title, :string
    field :body,  :string
    timestamps(type: :utc_datetime)
  end

  def changeset(post, params) do
    post
    |> cast(params, [:title, :body])
    |> validate_required([:title, :body])
    |> validate_length(:title, min: 1, max: 255)
  end
end

